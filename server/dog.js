
class Dog {
  constructor(id, name, breed, age, size, description, location, gender, coat, color, traits, photos, url) {
    this.id = id;
    this.name = name;
    this.breed = breed;
    this.age = age;
    this.size = size;
    this.description = description;
    this.location = location;
    this.gender = gender;
    this.coat = coat;
    this.color = color;
    this.traits = traits;
    this.photos = photos;
    this.url = url;
  }
}
module.exports = Dog;
